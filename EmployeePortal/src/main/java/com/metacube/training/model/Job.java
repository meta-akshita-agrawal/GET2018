package com.metacube.training.model;

public class Job {
	private int jobCode;
	private String title;
	
	public void setJobCode(int jobCode) {
		this.jobCode = jobCode;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getJobCode() {
		return jobCode;
	}
	public String getTitle() {
		return title;
	}
}
